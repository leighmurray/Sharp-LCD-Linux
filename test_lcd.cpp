#include "SharpLCD.hpp"
#include <iostream>

SharpLCD lcd;

int main(void) {

  const uint8_t tx[] = {
    0xFF, 0xFF, 0xFF, 0xAA, 0xFF, 0xFF,
    0x40, 0x00, 0x00, 0x00, 0x00, 0x95,
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
    0xDE, 0xAD, 0xBE, 0xEF, 0xBA, 0xAD,
    0xF0, 0x0D,
  };


  while (true) {
    lcd.writeLine(tx);
    usleep(1000000);
  }
  return 0;
}
